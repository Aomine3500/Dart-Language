main() {
  //Learn Functions in Dart
  //Types of Functions

  //We Have  Void and Return Functions
  //void Function
  sumNumbers(int a, int b) {
    int c = a + b;
    print(c);
  }

  sumNumbers(11, 28);

  //Return Funcion
  int multiNumbers(int x, int y) {
    return (x * y);
  }

  int z = multiNumbers(5, 8);
  print(z);
}
