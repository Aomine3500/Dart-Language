void main() {
  //Learn Data Type in Dart
  //1.String
  String name = "Ahmed";
  String lastname = "ZNOUDA";
  print("$name $lastname"); //print(name + " " + lastname);
  //Dart is sensible for spaces

  //2.Integer
  int age = 25;
  print("$age");

  //3.Double
  double Moy = 18.45;
  print("$Moy");

  //4.Boolean
  bool Verif = true;
  print(Verif);
}
