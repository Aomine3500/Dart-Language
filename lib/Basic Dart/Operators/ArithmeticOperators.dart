void main() {
  //Learn operators in Dart
  int a = 5;
  int b = 6;

  //Arithmetic oprators
  int c = a + b;
  print(c);
  int d = c - a;
  print(d);
  int e = c * d;
  print(e);
  double f = e / b;
  print(f);
  int g = e ~/ a;
  print(g);
  int h = e % c;
  print(h);

  //increment & decrement
  a++;
  b--;
  print(a);
  print(b);
}
