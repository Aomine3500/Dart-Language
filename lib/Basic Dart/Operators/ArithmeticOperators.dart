void main() {
  //Learn operators in Dart
  int a = 15;
  int b = 6;

  //Arithmetic oprators
  int c = a + b;
  print("The addition is $c");
  int d = a - b;
  print("The subtraction is $d");
  int e = a * b;
  print("The multiplication is $e");
  double f = a / b;
  print("The division is $f");
  int g = a ~/ b;
  print("The integer division is $g");
  int h = a % b;
  print("The modulus is $h");

  //increment & decrement
  a++;
  b--;
  print(a);
  print(b);
}
