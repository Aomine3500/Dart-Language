void main() {
  // Learn Operators in dart
  int a = 18;
  int b = 20;

//Check equality: return true or false
  print(a == b); //equal to
  print(a > b); //bigger than
  print(a >= b); // bigger or equal to
  print(a < b); //less than
  print(a <= b); //less or equal to
  print(a != b); //different to
}
